package com.example.myapplication;

public class TT {
    private int id;
    private String tt;

    public TT(int id, String tt) {
        this.id = id;
        this.tt = tt;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTt() {
        return tt;
    }

    public void setTt(String tt) {
        this.tt = tt;
    }
}
